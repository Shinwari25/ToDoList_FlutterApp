import mongoose from "mongoose";

const { Schema } = mongoose;

const toDoSchema = new Schema(
  {
    userId: {
      type: Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
    title: {
      type: String,
      required: true,
      trim: true,
    },
    description: {
      type: String,
      required: true,
      trim: true,
    },
    completed: {
      type: Boolean,
      default: false,
    },
  },
  { timestamps: true }
);

const ToDoModel = mongoose.model("ToDo", toDoSchema);

export default ToDoModel;
